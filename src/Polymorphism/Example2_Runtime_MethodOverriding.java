package Polymorphism;

public class Example2_Runtime_MethodOverriding {
	
	public void Name()
	{
		System.out.println("My name is vaishali");
	}
	public void Car()
	{
		System.out.println("Altroz");
	}
	
	public void Home()
	{
		System.out.println("Home");
	}
	
	public static void Money()
	{
		System.out.println("1 l");
	}
	

}
